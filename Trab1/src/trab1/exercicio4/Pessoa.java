public class Pessoa {

    private String nome;
    private int idade;
    private String mae;
    private String pai;

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public void setMae(String mae) {
        this.mae = mae;
    }

    public void setPai(String pai) {
        this.pai = pai;
    }

    public String getNome() {
        return this.nome;
    }

    public int getIdade() {
        return this.idade;
    }

    public String getMae() {
        return this.mae;
    }

    public String getPai() {
        return this.pai;
    }

}
